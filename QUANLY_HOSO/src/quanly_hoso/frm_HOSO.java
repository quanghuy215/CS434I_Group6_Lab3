        txtHoTen.setText(hs.getHoTen());
        txtNgaySinh.setText(df.format(hs.getNgaySinh()));
        if (hs instanceof GIAOVIEN) {
            GIAOVIEN gv = (GIAOVIEN) hs;
            cbGiaoVien.setSelected(true);
            cbHocSinh.setSelected(false);
            setPanelEnabled(pnGiaoVien, true);
            setPanelEnabled(pnHocSinh, false);
            setPanelEnabled(pnGV, true);
            setPanelEnabled(pnHs, false);
            txtMaGV.setText(gv.getMaGV());
            txtDanhGiaXL.setText(gv.DanhGiaXepLoai());
            txtNgayVao.setText(df.format(gv.getNgayVao()));
            txtDiemThiDua.setText(nf.format(gv.getDiemThiDua()));
            txtHSL.setText(nf.format(gv.getHSL()));
            txtPhuCap.setText(nf.format(gv.PhuCap()));
            txtLuong.setText(nf.format(gv.Luong()));
        }
        if (hs instanceof HOCSINH) {
            HOCSINH hsi = (HOCSINH) hs;
            cbHocSinh.setSelected(true);
            cbGiaoVien.setSelected(false);
            setPanelEnabled(pnGiaoVien, false);
            setPanelEnabled(pnHocSinh, true);
            setPanelEnabled(pnGV, false);
            setPanelEnabled(pnHs, true);
            txtDiemHoa.setText(nf.format(hsi.getDiemHoa()));
            txtDiemToan.setText(nf.format(hsi.getDiemToan()));
            txtDiemLy.setText(nf.format(hsi.getDiemLy()));
            txtMaHocSinh.setText(hsi.getMaHS());
            txtDiemTB.setText(nf.format(hsi.DTB()));
            txtXepLoai.setText(hsi.DanhGiaXepLoai());
            txtHocBong.setText(nf.format(hsi.TinhHocBong()));
        }
