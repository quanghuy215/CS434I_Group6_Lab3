    private void XoaDuLieu() {
        while (dtmHoSo.getRowCount() > 0) {
            dtmHoSo.removeRow(0);
        }
    }
    
    private String NgheNghiep(HOSO hs){
        if(hs instanceof HOCSINH){
            return "Học sinh";
        }else {
            return "Giáo viên";
        }
    }
    private void ThemMotDong(HOSO hs) {
        SimpleDateFormat df = new SimpleDateFormat("dd/MM/yyyy");
        dtmHoSo.addRow(new Object[]{
            hs.getMaHoSo(), hs.getHoTen(), hs.getSoCMND(), df.format(hs.getNgaySinh()), NgheNghiep(hs)
        });
    }

    private void NapDuLieu() {
        XoaDuLieu();
        for (HOSO hs : dsHoSo.getDs()) {
            ThemMotDong(hs);
        }
    }

