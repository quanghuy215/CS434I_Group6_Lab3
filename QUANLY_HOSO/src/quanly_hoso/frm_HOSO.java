    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        // TODO add your handling code here:
        Date ngaySinh = new Date();
        Date ngayVao = new Date();
        SimpleDateFormat df = new SimpleDateFormat("dd/MM/yyyy");
        try {
            ngaySinh = df.parse(txtNgaySinh.getText());
        } catch (ParseException ex) {
            Logger.getLogger(frm_HOSO.class.getName()).log(Level.SEVERE, null, ex);
        }
        try {
            ngayVao = df.parse(txtNgayVao.getText());
        } catch (ParseException ex) {
            Logger.getLogger(frm_HOSO.class.getName()).log(Level.SEVERE, null, ex);
        }
        if (cbHocSinh.isSelected()) {
            setGiaoVienNull();
            HOSO hs = new HOCSINH(txtMaHocSinh.getText(), Double.parseDouble(txtDiemToan.getText()), Double.parseDouble(txtDiemLy.getText()), Double.parseDouble(txtDiemHoa.getText()), txtMaHS.getText(), txtHoTen.getText(), Long.parseLong(txtCMND.getText()), ngaySinh);
            dsHoSo.Them(hs);
            NapDuLieu();
        }
        if (cbGiaoVien.isSelected()) {
            setHocSinhNul();
            HOSO gv = new GIAOVIEN(txtMaGV.getText(), ngayVao, Double.parseDouble(txtDiemThiDua.getText()), Double.parseDouble(txtHSL.getText()), txtMaHS.getText(), txtHoTen.getText(), Long.parseLong(txtCMND.getText()), ngaySinh);
            dsHoSo.Them(gv);
            NapDuLieu();
        }
    }//GEN-LAST:event_btnThemActionPerformed

